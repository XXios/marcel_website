// Lookup helper — returns the icon def for a given key, falls back to first icon
export function getIcon(key: string): IconDef {
  return ICONS.find((i) => i.key === key) || ICONS[0];
}

// Render an icon by key — convenience for components
export function renderIcon(key: string, className?: string): React.ReactElement {
  return getIcon(key).svg(className);
}
